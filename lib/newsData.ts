// 获取指定日期的新闻
export function getNewsByDate(date: string): DailyNews | null {
  return newsDatabase[date] || null
}

// 获取所有可用日期
export function getAvailableDates(): string[] {
  return Object.keys(newsDatabase).sort().reverse()
}

// 获取最新日期的新闻
export function getLatestNews(): DailyNews {
  const dates = getAvailableDates()
  const latestDate = dates[0]
  return newsDatabase[latestDate]
}

